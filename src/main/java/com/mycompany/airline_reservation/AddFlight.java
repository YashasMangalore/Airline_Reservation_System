    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try 
        {
            String flightID=FlightID.getText();
            String flightName=FlightName.getText();
            String arrival=Arrival.getText();
            String departure=Departure.getText();
            String duration=Duration.getText();
            String seats=Seats.getText();
            String fare=Fare.getText();
            DateFormat da=new SimpleDateFormat("yyyy-MM-dd");
            String date=da.format(DateOfFlight.getDate());
            
            // TODO add your handling code here:
            Connection con;//Stores connection between database & code
            PreparedStatement pre; //is used to prepare somequeries running in sql server
            Class.forName("com.mysql.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost/airline_reservation","root","Bajal123");
            pre=con.prepareStatement("insert into flight(flight_id,name,origin,destination,duration,seats,fare,date)values(?,?,?,?,?,?,?,?)");//in sql
            pre.setString(1, flightID);
            pre.setString(2, flightName);
            pre.setString(3, arrival);
            pre.setString(4, departure);
            pre.setString(5, duration);
            pre.setString(6, seats);
            pre.setString(7, fare);
            pre.setString(8, date);
           
            pre.executeUpdate();
            JOptionPane.showMessageDialog(null,"Flight added successfully");
            this.setVisible(false);
        } 
        catch (ClassNotFoundException | SQLException ex) 
        {
            Logger.getLogger(AddFlight.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed


